    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Enter UserName");
        }else if(!jPasswordField1.getText().equalsIgnoreCase(jPasswordField2.getText())){
            JOptionPane.showMessageDialog(this, "Passsword doesn't Match ");
        }else if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Enter Name");
        }else if(jTextField4.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please Enter Security Answer");
        }  else{
            try{
            String uname = jTextField1.getText();
            String Name = jTextField2.getText();
            int password = Integer.parseInt(jPasswordField2.getText());
            String sec_Q = (String)jComboBox1.getSelectedItem();
            String ans = jTextField4.getText();
            String query = "Insert into librarian_account(Username ,l_name,l_password,Sec_Q,answer) values(?,?,?,?,?)";
            ps=con.prepareStatement(query);
            ps.setString(1, uname);
            ps.setString(2, Name);
            ps.setInt(3, password);
            ps.setString(4, sec_Q);
            ps.setString(5, ans);
            ps.execute();
            JOptionPane.showMessageDialog(this, "New User Added Successfully!");
            rs.close();
            ps.close();
            }catch(Exception e){
               
            }
            
        }   
        
    }//GEN-LAST:event_jButton1ActionPerformed
    
